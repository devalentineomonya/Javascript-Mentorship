// Example 1: Create and Log Variables
let favoriteFood = "Pizza";
let favoriteNumber = 7;
console.log("Favorite Food:", favoriteFood);
console.log("Favorite Number:", favoriteNumber);

// Example 2: Arithmetic with Variables
let num1 = 20;
let num2 = 5;

// Add two numbers
let sum = num1 + num2;
console.log("Sum:", sum); // Outputs: Sum: 25

// Multiply two numbers
let product = num1 * num2;
console.log("Product:", product); // Outputs: Product: 100

// Advanced: Calculating percentage
let total = 100;
let percentage = (num1 / total) * 100;
console.log("Percentage:", percentage + "%"); // Outputs: Percentage: 20%

